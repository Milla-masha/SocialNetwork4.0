package sjc.app.model.vo;

public class MusicVO extends BaseVO
{

    private String name;
    private String url;

    public MusicVO(String url, String name) {
        this.url = url;
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
